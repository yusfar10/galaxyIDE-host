const container = document.querySelector(".content-best");
const prevButton = document.getElementById("prev");
const nextButton = document.getElementById("next");

// Fungsi untuk menggulir ke kiri
prevButton.addEventListener("click", () => {
  container.scrollBy({
    left: -300, // Jarak scroll ke kiri (sesuaikan dengan kebutuhan)
    behavior: "smooth", // Animasi scroll
  });
});

// Fungsi untuk menggulir ke kanan
nextButton.addEventListener("click", () => {
  container.scrollBy({
    left: 300, // Jarak scroll ke kanan (sesuaikan dengan kebutuhan)
    behavior: "smooth", // Animasi scroll
  });
});




